// This program calculates and displays the area and
// perimeter of a rectangle with dimensions 10cm X 8cm.
#include <iostream>

int main() {
  std::cout << "For a rectangle that has dimensions\n";
  std::cout << "length 10cm and width 8cm:\n";
  std::cout << "\n";
  std::cout << "Area = " << 10 * 8 << " cm^2\n";
  std::cout << "Perimeter = " << 2* (10 + 8) << " cm\n";
}
